# Kiêu dữ liệu dạng số
print(2)
print(3.1121231)
print(-73648726)
print(2+3)
print(2*(3+4))
print(5%2)
number = 2
print(number)
# str() biến đổi số thành chuỗi trước khi cộng vào chuỗi khác
print(str(number) + " là số người yêu mà mày từng có")
#abs() tính giá trị tuyệt đối của 1 chuỗi
print(abs(number))
#pow(số,mũ) số mũ
print(pow(2,-3))
#max(s1,s2) số lớn nhất trong 2 số đầu vào
print(max(2,10))
#min(s1,s2) giống max nhưng trả về số nhỏ nhất
print(min(2,10))
#round(s1) làm tròn
print(round(number))

import math # gọi thư viện toán học
#math.floor() làm tròn xuống
print(math.floor(number))
#math.ceil() làm tròn lên
print(math.ceil(number))
#math.sqrt() tính căn bậc 2
print(math.sqrt(number))